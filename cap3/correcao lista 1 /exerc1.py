def adicao(n1, n2):
    return n1 + n2

def subtracao(n1, n2):
    return n1 - n2

def multiplicacao(numero1,  numero2):
    return n1 * n2

def divisao(numero1,  numero2):
    return n1 / n2

# main
n1 = int(input("digite o primeiro numero:"))
n2 = int(input("digite o primeiro numero:"))

resultadoAdicao = adicao(n1, n2)
resultadoSubtracao = subtracao(n1, n2)
resultadomultiplicacao = multiplicacao(n1, n2)
resultadodivisao = divisao(n1, n2)

print("adicao", resultadoAdicao)
print("subtracao", resultadoSubtracao)
print("multiplicacao", resultadomultiplicacao)
print("divisao", resultadodivisao)